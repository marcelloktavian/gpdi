                                            <h3 class="bold-title">Statement of Faith (Pengakuan Iman GPdI)</h3>
                                                <p>1. &nbsp;&nbsp; Kami percaya Alkitab adalah Firman Allah yang diilhamkan oleh Roh Kudus terdiri dari 66 buku “Kejadian sampai dengan Wahyu” (II Timotius 3 : 16; II Petrus 1 : 21).</p>
                                               
                                                <p>2. &nbsp;&nbsp; Kami percaya Allah Yang Maha Esa dan kekal dalam wujud Trinitas : “BAPA dan PUTERA dan ROH KUDUS”, ( Ulangan 6 : 4; I Timotius 2 : 5; I Yohanes 5:7; Matius 28 : 19 ), Keesaan namaNya yaitu : “ TUHAN YESUS KRISTUS“, ( Kisah Para Rasul 2 : 36; 8 : 12; 10 : 48; Matius 1 : 1; Wahyu 22 : 20 – 21; Kisah Para Rasul 19:5; I Petrus 3:15 ).</p>
                                                
                                                <p>3. &nbsp;&nbsp; Kami percaya Allah pencipta alam semesta dan manusia, seperti tertulis dalam Kitab Kejadian ( Kejadian 1 dan 2; Yohanes 1:1-3; Kolose 1:16; Roma 4:17; Roma 1:19-20)<br>
                                                
                                                <p>4. &nbsp;&nbsp; Kami percaya Tuhan Yesus Kristus , Anak Allah yang telah menjadi manusia, dilahirkan Perawan Maria yang mengandung oleh Roh Kudus, mati disalib, menanggung dosa manusia, dikuburkan, bangkit, naik ke surga dan akan datang kembali. ( Yohanes 20 : 31; Roma 1 : 4; I Yohanes 4 : 15; Yohanes 1 : 14; Filipi 2 : 7-8; II Timotius 3 :16; Matius 1 : 18; Yesaya 7 : 14; Lukas 1 : 35; I Timotius 1 : 15; Kisah Para Rasul 4 : 1 – 12; 10 : 42- 43; Roma 6 : 4; I Korintus 15 : 3-4; I Tesalonika 4 : 15, 17 ).</p>
                                                
                                                <p>5. &nbsp;&nbsp; Kami percaya Roh Kudus adalah Pribadi Allah yang memiliki sifat: Kekal, Mahahadir, Mahakuasa, Mahatahu, Mahakudus, Mahakasih dan baptisan Roh Kudus yaitu kepenuhan Roh Kudus dengan tanda berkata-kata dalam berbagai bahasa sebagaimana diilhamkan oleh Roh Kudus diterima oleh orang percaya, bertobat dan lahir baru (I Yohanes 5 : 7; II Korintus 13 : 13; Ibrani 9 : 14; Mazmur 139 : 7-10; Lukas 1 : 35; Kejadian 1 : 2; Ayub 26 : 13; Kisah Para Rasul 2 : 4; 10 : 45-46; 19 : 6; Markus 16 : 17; Yohanes 7 : 38-39).</p>
                                             
                                                <p>6. &nbsp;&nbsp; Kami percaya baptisan air, yaitu diselamkan dalam nama Bapa dan Putera dan Roh Kudus, yaitu Tuhan Yesus Kristus wajib dilakukan bagi mereka yang diselamatkan yaitu percaya, bertobat dan lahir baru, untuk menggenapkan kebenaran Allah. ( Markus 16 : 15-16; Kisah Para Rasul 2 : 38; 8 : 12, 37 dan 39; Matius 3 : 15; 28 : 19; Markus 1:15).</p>
                                                
                                                <p>7. &nbsp;&nbsp; Kami percaya keselamatan orang berdosa, roh, jiwa dan tubuh, oleh anugerah dan iman kepada Tuhan Yesus Kristus, dan semua orang percaya harus mempertahankan keselamatan, kekudusan, kesetiaan dan apabila tidak memeliharanya, keselamatan itu dapat hilang ( Efesus 2 : 8-9; Roma 10 : 9-10; I Korintus 1 : 18; Filipi 2:12; Matius 24:13; Ibrani 3:12; II Petrus 2: 20-22; 1: 4-11; Yudas 1:3 ).</p>
                                                
                                                <p>8. &nbsp;&nbsp; Kami percaya peranan karunia–karunia Roh Kudus dalam Jemaat. (I Korintus 12: 4 – 11; 14:26).</p>
                                                
                                                <p>9. &nbsp;&nbsp; Kami percaya Perjamuan Tuhan yang lazim disebut Perjamuan Kudus harus diterima oleh mereka yang percaya. ( Lukas 22: 19-20; I Korintus 11:23-26; Yohanes 6:53 – 56).</p>
                                                
                                                <p>10. &nbsp;&nbsp; Kami percaya kesembuhan Allahi atas segala penyakit oleh bilur–bilur Yesus dalam kuasa nama-Nya. (Yesaya 53:4; I Petrus 2:24; Kisah Para Rasul 4:30; Markus 16:18).</p>
                                                
                                                <p>11. &nbsp;&nbsp; Kami percaya penyerahan anak-anak adalah kehendak Tuhan. (Lukas 2:22–27; Matius 19:13–15; Markus 10:13–16; Lukas 18:15–17).</p>
                                                
                                                <p>12. &nbsp;&nbsp; Kami percaya Gereja Tuhan yang esa, persekutuan orang-orang percaya, kudus dan sempurna sebagai Mempelai Perempuan, disingkirkan selama masa tiga setengah tahun tribulasi, diubahkan dan diangkat pada kedatangan kembali Tuhan Yesus. (Yohanes 17:21–23; Efesus 4:12–16; I Tesalonika 5:23; I Petrus 5:10; I Tesalonika 5:4; I Korintus 15: 51).</p>
                                                
                                                <p>13. &nbsp;&nbsp; Kami percaya Tuhan Yesus Kristus sebagai Mempelai Laki–laki, Raja atas segala raja dan Tuan atas segala tuan, yang akan datang untuk menghukum isi dunia dengan adil, dan akan memerintah dalam Kerajaan Seribu Tahun Damai bersama Mempelai Perempuan yaitu Gereja-Nya. (Kisah Para Rasul 1:11; Wahyu 22:7; I Korintus 15:24–25; I Tesalonika 4:16–17; II Tesalonika 1:7-9; Wahyu 20:10–15; Wahyu 19:11-16; I Timotius 6:15).</p>
                                                
                                                <p>14. &nbsp;&nbsp; Kami percaya kebangkitan orang–orang kudus sebelum Kerajaan Seribu Tahun Damai dan kebangkitan orang–orang berdosa sesudah Kerajaan itu; orang kudus akan menerima hidup kekal, orang berdosa akan menghadap tahta Allah untuk menerima pehukuman kekal dalam lautan api. (Wahyu 20:1–15; I Tesalonika 4:16–17).</p>
                                                
                                                <p>15. &nbsp;&nbsp; Kami percaya langit dan bumi baru yang berisi Kebenaran, tempat kediaman kekal umat tebusan darah Kristus (I Petrus I:18-19; II Petrus 3:13; Wahyu 21: 1–18).</p>

                                                <p>16. &nbsp;&nbsp; Kami percaya pertemuan–pertemuan ibadah, wajib dilaksanakan secara tetap dengan khidmad dan sukacita. (Kisah Para Rasul 2:25; Keluaran 23:25; Ibrani 10:25; Mazmur 47:2; 100:1-5; 134:2; 150:1-5).</p>
                                                
                                                <p>17. &nbsp;&nbsp; Kami percaya setiap pemerintah adalah hamba Allah yang ditetapkan Allah. (Roma 13:4; I Petrus 2:17; I Timotius 2:1-2; Amsal 21:1).</p>
