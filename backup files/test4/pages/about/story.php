                                                <h3 class="bold-title">History Of GPdI</h3>
                                                <p>Berdirinya Gereja Pantekosta di Indonesia tidak terlepas dari kedatangan dua keluarga missionaris dari Gereja Bethel Temple Seattle, USA ke Indonesia pada tahun 1921 yaitu Rev. Cornelius Groesbeek dan Rev. Richard Van Klaveren keturunan Belanda yang berimigrasi ke Amerika.</p>

                                                <p>Dari Bali maka pelayanan beralih ke Surabaya di pulau Jawa tahun 1922, kemudian ke kota minyak Cepu pada tahun 1923. Di kota inilah F.G Van Gessel pegawai BPM bertobat dan dipenuhkan Roh Kudus disertai/disusul banyak putera – puteri Indonesia lainnya antara lain : H.N. Runkat, J. Repi, A. Tambuwun, J. Lumenta, E. Lesnusa, G.A Yokom, R.Mangindaan, W. Mamahit, S.I.P Lumoindong dan A.E. Siwi yang kemudian menjadi pionir-pionir pergerakan Pantekosta di seluruh Indonesia.</p>

                                                <p>Karena kemajuan yang pesat, maka pada tanggal 4 Juni 1924 Pemerintah Hindia Belanda mengakui eksistensi “De Pinkster Gemeente in Nederlansch Indie” sebagai sebuah “Vereeniging” (perkumpulan) yang sah. Dan oleh kuasa Roh Kudus serta semangat pelayanan yang tinggi, maka jemaat-jemaat baru mulai bertumbuh dimana-mana.</p>

                                                <p>Tanggal 4 Juni 1937, pemerintah meningkatkan pengakuannya kepada pergerakan Pantekosta menjadi “Kerkgenootschap” (persekutuan gereja) berdasarkan Staatblad 1927 nomor 156 dan 523, dengan Beslit Pemerintah No.33 tanggal 4 Juni 1937 Staadblad nomor 768 nama “pinkster Gemente” berubah menjadi “Pinksterkerk in Nederlansch Indie”.</p>

                                                <p>Pada zaman pendudukan Jepang tahun 1942, nama Belanda itu diubah menjadi “Gereja Pantekosta di Indonesia”. Ketika itu Ketua Badan Pengoeroes Oemoem ( Majelis Pusat) adalah Pdt. H.N Runkat.</p>

                                                <p>Selain perkembangan perlu juga dicatat beberapa perpecahan yang kemudian melahirkan gereja-gereja baru dimana para pendirinya berasal dari orang-orang GPdI antara lain: Pdt. Ho Liong Seng (DR.H.L Senduk) pendiri gereja GBI yang bersama Pdt. Van Gessel pada tahun 1950 berpisah dengan GPdI dan mendirikan GBIS, Pdt. Ishak Lew pada tahun 1959 keluar dan mendirikan GPPS, sebelumnya pada tahun 1936 Missionaris R.M. Devin dan R. Busby keluar dan membentuk Assemblies of God, tahun 1946 Pdt. Tan Hok Tjoan berpisah dan membentuk Gereja Isa Almasih dan lain-lain sebagainya.</p>

                                                <p>Peranan para pioner pun patut dikenang, sebab karena perjuangan mereka pohon GPdI telah bertumbuh dengan lebat, mereka antara lain: Pdt. H.N. Runkat yang merambah ladang di Pulau Jawa, (Jakarta, Jabar, Jateng, dll), tahun 1929 Pdt. Yulianus Repi dan Pdt. A. Tambuwun disusul oleh Pdt. A. Yokom, Pdt. Lumenta, Pdt. Runtuwailan menggempur Sulawesi Utara, tahun 1939, dari Sulut / Ternante Pdt. E. Lesnussa ke Makasar dan sekitarnya. Tahun 1926 Pdt. Nanlohy menjangkau kepulauan Maluku (Amahasa) yang kemudian disusul oleh Pdt. Yoop Siloey, dll.</p>

                                                <p>Tahun 1928 Pdt. S.I.P Lumoindong ke D.I Yogyakarta tahun 1933 Pdt. A.E. Siwi menabur ke pulau Sumatera (Sumsel, Lampung, Sumbar dan kemudian tahun 1939 ke Sumut), tahun 1932 Pdt. RM Soeprapto mulai membantu pelayanan di Blitar kemudian Singosari dsk, tahun 1937 ke Sitiarjo Malang Selatan.</p>

                                                <p>Tahun 1935 Pdt. Siloey dkk, merintis pelayanan ke Kupang NTT, tahun 1930 Pdt. De Boer disusul Pdt. E. Pattyradjawane dan A.F Wessel ke Kalimantan Timur.</p>

                                                <p>Tahun 1940 Pdt. JMP Batubara menebas ladang Kalimantan Barat (Pontianak), Pdt. Yonathan Itar pelopor Injil Pantekosta di Irian Jaya, dan lain-lain yang tidak dapat disebutkan satu per satu. Oleh pengorbanan mereka GPdI bertumbuh dengan pesat.</p>
